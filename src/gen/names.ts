import { randChoice } from '../utils';

export function genHumanFirstName(): string {
    return randChoice(FIRST_NAMES);
}

export function genHumanLastName(): string {
    return randChoice(LAST_NAMES);
}

export function genHumanName(): string {
    const r = Math.random();
    const middle = r > 0.9
        ? `"${randChoice(NICKNAMES)}" `
        : r > 0.5
            ? `${randChoice(MIDDLE_INITIALS)}. `
            : "";
    return `${genHumanFirstName()} ${middle}${genHumanLastName()}`;
}

